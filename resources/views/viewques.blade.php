    <p>Scenario: In a kinder garden, children can be picked up from there by their father, mother, brother, sister, grandfather and grandmother only.</p>
    <br>
    Task: Create an application to insert the child data into the system by the following
    <br>
    manners:<br>

    1). The registration form should have fields- child name, date of birth (date picker), class

    (dropdown from I-XII), Address, City, State (dropdown- it can be populated depending upon

    country data), Country (dropdown), Zip Code (7-digit number), upload child photo/picture

    (JPG, JPEG or PNG with dimension minimum 100x100 PX and size maximum 1 MB)
    <br>

    2). There will be a single row form for the picked-up person details with the fields picked up

    person name, relation (dropdown like Father, Mother, Brother, Sister, Grand Father, Grand

    Mother), contact no (10-digit mobile number).

    <br>

    3). There will be "Add More" option by which more picked up person can be added.<br>

    4). Minimum 1 and maximum 6 picked up person can be added.<br>

    5). There will be row remove option with respect to each row.<br>

    6). Put appropriate client-side and server-side validations for all fields.<br>

    7). After successful registration, thank-you message should be displayed along with all the

    registered data.<br>

